function setupIntersectionObserver() {
    const elements = document.querySelectorAll(".short-video");
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(playVideoInViewport);
    }, intersectionOptions);
    elements.forEach((element) => {
        observer.observe(element);
    });
}

function toggleVideoPlay(event) {
    if (event.target.classList.contains("action-button")) {
        return; // Don't toggle video play if clicked on an action button
    }
    const video = this.querySelector("video");
    if (video.paused) {
        video.play();
    } else {
        video.pause();
    }
}

function setupClickFunctionality() {
    const videoContainers = document.querySelectorAll(".short-video");
    videoContainers.forEach((container) => {
        container.addEventListener("click", toggleVideoPlay);
    });
}

function handleScroll() {
    const elements = document.querySelectorAll(".short-video");
    const containerHeight = shortsContainer.offsetHeight;
    const scrollPosition = shortsContainer.scrollTop;

    let lastVisibleIndex = -1;
    for (let i = 0; i < elements.length; i++) {
        const element = elements[i];
        const elementTop = element.offsetTop;
        if (elementTop >= scrollPosition && elementTop < scrollPosition + containerHeight) {
            lastVisibleIndex = i;
        }
    }

    const secondVideoFromLast = lastVisibleIndex === elements.length - 2;
    if (secondVideoFromLast && lastVisibleIndex > lastVisibleVideoIndex) {
        lastVisibleVideoIndex = lastVisibleIndex;

        startIndex = endIndex;
        endIndex = startIndex + videosPerPage;

        if (endIndex <= videoDataArray.length) {
            currentPage++;
            console.log(`Page: ${currentPage}`);
            updateDOMWithVideos();
        }
    }
}

function debounce(func, delay) {
    let timer;
    return function () {
        clearTimeout(timer);
        timer = setTimeout(func, delay);
    };
}

